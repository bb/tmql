package de.topicmapslab.tmql4j.flwr.components.processor.results.ctm;

import org.tmapi.core.TopicMap;
import org.tmapi.core.TopicMapSystem;

import de.topicmapslab.tmql4j.components.processor.results.Result;
import de.topicmapslab.tmql4j.components.processor.results.ResultSet;
import de.topicmapslab.tmql4j.exception.TMQLRuntimeException;
import de.topicmapslab.tmql4j.flwr.util.CTMConverter;

/**
 * Result implementation representing a CTM fragment. The result is generated by
 * the interpretation of tm-content expressions.
 * 
 * @author Sven Krosse
 * @email krosse@informatik.uni-leipzig.de
 * 
 */
public class CTMFragment extends Result {

	/**
	 * base constructor to create an empty result
	 * 
	 * @param parent
	 *            the parent result set
	 */
	public CTMFragment(ResultSet<?> parent) {
		super(parent);
	}

	/**
	 * {@inheritDoc}
	 */
	public String toString() {
		return size() == 0 ? "" : getResults().get(0).toString();
	}

	/**
	 * Convert the contained CTM fragment to a topic map.
	 * 
	 * @param system
	 *            the topic maps system containing the generated topic map
	 * @return the topic map as representation of the CTM fragment
	 * @throws TMQLRuntimeException
	 *             thrown if generation failed
	 */
	public TopicMap toTopicMap(final TopicMapSystem system) throws TMQLRuntimeException {
		return CTMConverter.toTopicMap(first().toString(), system);
	}

}
